
public class Variables {

	public static void main(String[] args) {
		
		byte edad; //Reservo un espacio en memoria para almacenar el numero 35
		
		edad=35; //Inicializo la variable
		
		byte edad1=40; //Reservo un espacio en memoria e inicializo la misma en la misma linea
		
		// Los espacios verticales que dejemos en el codigo NO tiene relevancia en la ejecucion del codigo
		
		System.out.println(edad); //Imprime en consola el valor de edad en esta etapa del programa
		System.out.println(edad1); //Imprime en consola el valor de edad1 en esta etapa del programa
		
		edad1=89; //Modifico el valor de la variable edad1 despues del la primera impresion y lo vuelvo a imprimir. Importante ver como es el flujo de ejecucion del programa
				
		System.out.println(edad1); //Imprime en consola el valor de edad1 en esta etapa del programa

	}

}
